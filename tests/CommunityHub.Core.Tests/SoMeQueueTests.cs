    // ----- auto-vs-static override ------------------------------------------

    [Fact]
    public async Task Manual_override_wins_over_auto_text_and_refresh_never_flushes_it()
    {
        using var db = TestDb.New();
        var (eventId, _, speakerId, sessionId) = await SeedAsync(db);
        var queue = NewQueue(db);

        var post = await queue.CreateSpeakerPostAsync(
            eventId, speakerId, sessionId, Now.AddDays(2), autoGenerate: true, byEmail: "org@example.test");

        await queue.EditAsync(eventId, post.Id, "My hand-written post", null, "org@example.test");
        var edited = await queue.GetAsync(eventId, post.Id);
        Assert.Equal("My hand-written post", edited!.EffectiveText);

        // A refresh re-populates auto text but the override still wins.
        await queue.RefreshAutoAsync(eventId, post.Id);
        var refreshed = await queue.GetAsync(eventId, post.Id);
        Assert.Equal("My hand-written post", refreshed!.EffectiveText);
        Assert.Contains("Sam Speaker", refreshed.AutoText);  // auto text refreshed underneath

        // Clearing the override falls back to auto text.
        await queue.EditAsync(eventId, post.Id, "", null, "org@example.test");
        var cleared = await queue.GetAsync(eventId, post.Id);
        Assert.Contains("Sam Speaker", cleared!.EffectiveText);
    }

