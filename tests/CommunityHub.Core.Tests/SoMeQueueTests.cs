    private static BrandingGraphicRef Ref(string key, string imageRef) =>
        new(key, GraphicAssetType.Speaker, imageRef, "x.png", "draft");

    /// <summary>
    /// A fake <see cref="IBrandingGraphicsProvider"/> that returns ONLY what the
    /// test placed in it (the gate is modelled by simply not adding an entry =
    /// gated/un-released => null). Lets the SoMe wiring be asserted without the
    /// graphics engine / ImageSharp.
    /// </summary>
    private sealed class FakeBranding : IBrandingGraphicsProvider
    {
        public Dictionary<int, BrandingGraphicRef> Speaker { get; } = new();
        public Dictionary<(int, int), BrandingGraphicRef> Session { get; } = new();
        public Dictionary<string, BrandingGraphicRef> Sponsor { get; } = new();

        public Task<BrandingGraphicRef?> GetSpeakerGraphicAsync(
            int eventId, int participantId, CancellationToken ct = default) =>
            Task.FromResult(Speaker.TryGetValue(participantId, out var r) ? r : null);

        public Task<BrandingGraphicRef?> GetSessionGraphicAsync(
            int eventId, int sessionId, int participantId, CancellationToken ct = default) =>
            Task.FromResult(Session.TryGetValue((sessionId, participantId), out var r) ? r : null);

        public Task<BrandingGraphicRef?> GetSponsorGraphicAsync(
            int eventId, string sponsorCompanyId, CancellationToken ct = default) =>
