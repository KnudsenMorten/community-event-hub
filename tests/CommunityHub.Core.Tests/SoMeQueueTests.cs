    // ----- dispatch idempotency + gating ------------------------------------

    [Fact]
    public async Task Due_active_post_publishes_once_and_re_run_never_double_posts()
    {
        using var db = TestDb.New();
        var (eventId, _, _, _) = await SeedAsync(db);
        var queue = NewQueue(db);
        await NewSettings(db).SaveAsync(eventId, true, "urn:li:organization:1", null, null, false, null);
        var post = await queue.CreateAdHocPostAsync(
            eventId, "due now", null, Now.AddMinutes(-1), null, "org@example.test");

        var publisher = new RecordingPublisher();
        var dispatch = NewDispatch(db, publisher);

        var first = await dispatch.DispatchDueAsync(eventId);
        Assert.Equal(1, first.Published);
        Assert.Single(publisher.Posted);

        var published = await queue.GetAsync(eventId, post.Id);
        Assert.Equal(SoMePostStatus.Published, published!.Status);
        Assert.Equal("ext-1", published.ExternalPostId);

        // Second run: the status is the sent-marker — nothing re-publishes.
        var second = await dispatch.DispatchDueAsync(eventId);
        Assert.Equal(0, second.Published);
        Assert.Single(publisher.Posted);                 // still just one POST
    }

