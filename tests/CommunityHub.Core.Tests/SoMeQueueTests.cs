    }

    // ----- helper publishers ------------------------------------------------

    private sealed class RecordingPublisher : ILinkedInPostPublisher
    {
        public List<LinkedInPost> Posted { get; } = new();
        public bool CanPublish => true;
        public Task<LinkedInPublishResult> PublishAsync(LinkedInPost post, CancellationToken ct = default)
        {
            Posted.Add(post);
            return Task.FromResult(new LinkedInPublishResult(true, $"ext-{Posted.Count}", "ok"));
        }
    }

    private sealed class ThrowingPublisher : ILinkedInPostPublisher
    {
        public bool CanPublish => true;
        public Task<LinkedInPublishResult> PublishAsync(LinkedInPost post, CancellationToken ct = default)
            => throw new InvalidOperationException("kaboom");
    }
